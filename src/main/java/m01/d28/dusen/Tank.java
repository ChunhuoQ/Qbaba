package m01.d28.dusen;

/**
 *坦克类
 */
public class Tank {
    private int x;

    private int y;
    public Tank(){
        
    }
    public Tank(int x , int y){
        this.x=y;
        this.y=y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

}
