package m01.d28.Qbaba;

public class Taikei {
	private int x;
	private int y;
	public Taikei(){
		
	}
	public Taikei(int x,int y){
		this.x=x;
		this.y=y;
	}
	public int getX() {
		return x;
	}
	public void setX(int x) {
		this.x = x;
	}
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	
}
